var soma = 10 + 10;
soma //exibe o resultado 20 na tela

soma = soma + 1;
//resultado 21

//Operadores abreviados

//o variavel++ é a mesma coisa de soma = soma + 1, porém ele só funciona se chamar a variável mais uma vez
soma++;
//acrescenta + 1  = soma = 22

//o ++variavel já soma na hora, não precisa chamar de novo
++soma;
//exibe 23

soma = soma - 1;
//exibe 22

soma--;
//exibe 22

soma;
//agora soma é 21

--soma;
//soma = 20

soma;
//20

soma++
//20

soma;
//21

soma += 20;
//41

soma-= 20
//21

soma;
//21

